// Utility function to get complexity icon
export const getComplexityIcon = (complexity) => {
  if (complexity >= 8) return TreeIcon; // High complexity - stable structure needed
  if (complexity >= 5) return NetworkIcon; // Medium complexity - interconnected
  return ConnectorIcon; // Low complexity - simple connections
};

// Status icon mapping
export const getStatusIcon = (status) => {
  switch (status) {
    case 'completed':
      return CheckCircleIcon;
    case 'in_progress':
    case 'processing':
      return ClockIcon;
    case 'failed':
    case 'error':
      return ExclamationCircleIcon;
    case 'rejected':
      return XCircleIcon;
    default:
      return InformationCircleIcon;
  }
};

